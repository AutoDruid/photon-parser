func TestIsHeader(t *testing.T) {
	test := "4d4352483331313000000000000000"
	b, _ := hex.DecodeString(test)

	res, err := IsHeader(b)

	if err != nil {
		t.Fatal("not expected error for short payload, got nil")
	}

	if !res{
		t.Fatal("not expected error for short payload, got nil")
	}
}

